#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	size_t	i;

	i = 0;
	while (s1 && s2)
	{
		if (s1[i] == s2[i])
		{
			if (s1[i + 1] == '\0' && s2[i + 1] == '\0')
				return (1);
		}
		else
			return (0);
		i++;
	}
	return (0);
}
